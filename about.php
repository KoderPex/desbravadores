            </div>
            <div class="col-md-8">
                <p style="text-align:justify">Reunido debaixo de árvores e nas casas das pessoas, o movimento de desbravadores da igreja Aventista do Sétimo Dia do bairro de Capão Redondo denominado Pioneiros, iniciou suas atividades no anonimato no ano de 1959, sem nem mesmo saber da existência desse movimento. Seguindo um pequeno livreto MV para orientar as atividades, adolescentes e crianças nem sabiam o que estavam iniciando.</p>
                <p style="text-align:justify">Algum tempo depois é que realmente tomamos conhecimento de que mesmo sem saber, já fazíamos as atividades que outros clubes estavam fazendo nos Estados Unidos e no Peru. A partir daí o clube continuou simplesmente fazendo, sem dar importância a documentação e o registro histórico, e também não havia por parte da liderança da igreja local da época, qualquer orientação sobre isso, de como deveríamos proceder. Somente em 1961, com a vinda do Pr. Joel Sarli como pastor do distrito, é que o mesmo insistiu com a liderança local de que uma formalização era necessária, uma vez que as atividades precisavam estar de acordo com os manuais, orientações e regulamentação da instituição adventista mundial.</p>
