                <p style="text-align:justify">Iniciamos as atividades no mês de abril de 1959, influenciados pelos escoteiros da cidade, com o nome inicial de escoteiros adventistas, sem ter conhecimento que a igreja mundial já havia oficializado o programa.</p>
                <p style="text-align:justify">Com a chegada do Pr. Joel Sarli, vendo as atividades já praticadas pelo então grupo de escoteiros adventistas, o dilema entre os pais que não deixavam que as crianças participassem, apresentou então para a igreja local o programa dos desbravadores, já reconhecido pela Associação Geral. Em 1961, o programa foi apreciado cuidadosamente pela comissão local e foi aprovado. O clube que até então era informal, passou a ser um departamento oficial, adotando o nome de Pioneiros. Com a novidade, viu-se a necessidade de escolher um diretor, mas como o programa ainda era recente e não havia líderes formados para conduzir o clube, o Pr. Joel Sarli foi nomeado o primeiro diretor. Na época, os Desbravadores tinham mais facilidades e acesso a materiais e informações em relação a outros clubes que também surgiram no estado e no Brasil, porque a Associação Paulista ficava próxima.</p>
